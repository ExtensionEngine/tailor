<template>
  <div class="mb-5">
    <div class="actions">
      <v-btn
        @click="deleteContainer"
        color="error"
        outline
        class="pull-right">
        Delete {{ name }}
      </v-btn>
    </div>
    <v-alert
      :value="!teachingElements.length"
      color="primary"
      icon="mdi-information-variant"
      outline>
      Click the button below to create content.
    </v-alert>
    <tes-list
      @add="addElement"
      @insert="insert"
      @update="reorder"
      :list="teachingElements"
      :activity="container"
      :types="types"
      :layout="layout">
      <teaching-element
        slot="list-item"
        slot-scope="{ item, dragged, setWidth }"
        :set-width="setWidth"
        :dragged="dragged"
        :element="item"
        :element-style="getElementStyle(item.contentId)">
        <div :class="{ inactive: !hasActiveUsers(item.contentId) }" class="active-users-wrapper">
          <active-users
            v-if="getActiveUsers('element', item.contentId)"
            :users="getActiveUsers('element', item.contentId)"
            :size="26"
            vertical
            tooltip-right />
        </div>
      </teaching-element>
    </tes-list>
  </div>
</template>

<script>
import { mapActions, mapGetters } from 'vuex';
import ActiveUsers from 'components/common/ActiveUsers';
import filter from 'lodash/filter';
import first from 'lodash/first';
import sortBy from 'lodash/sortBy';
import TeachingElement from '../../TeachingElement';
import TesList from '../TesList';

export default {
  name: 'content-container',
  props: {
    container: { type: Object, required: true },
    types: { type: Array, default: null },
    name: { type: String, required: true },
    layout: { type: Boolean, required: true }
  },
  computed: {
    ...mapGetters('activeUsers', ['getActiveUsers']),
    ...mapGetters(['tes']),
    teachingElements() {
      const activityId = this.container.id;
      return sortBy(filter(this.tes, { activityId }), 'position');
    }
  },
  methods: {
    ...mapActions('tes', {
      reorderElements: 'reorder',
      insertElement: 'insert',
      addElement: 'save'
    }),
    reorder({ newIndex: newPosition }) {
      const items = this.teachingElements;
      const element = items[newPosition];
      const isFirstChild = newPosition === 0;
      const context = { items, newPosition, isFirstChild };
      this.reorderElements({ element, context });
    },
    insert(element) {
      const items = this.teachingElements;
      const { position: newPosition } = element;
      const isFirstChild = newPosition === -1;
      const context = { items, newPosition, isFirstChild, insert: true };
      this.insertElement({ element, context });
    },
    deleteContainer() {
      this.$emit('delete');
    },
    hasActiveUsers(elementId) {
      const activeUsers = this.getActiveUsers('element', elementId);
      return activeUsers.length;
    },
    getElementStyle(elementId) {
      const activeUsers = this.getActiveUsers('element', elementId);
      if (!activeUsers.length) return;
      const { palette, profileImage } = first(activeUsers);
      const color = palette[profileImage ? 'border' : 'background'];
      return { boxShadow: `0 0 0 2px ${color}` };
    }
  },
  components: {
    ActiveUsers,
    TesList,
    TeachingElement
  }
};
</script>

